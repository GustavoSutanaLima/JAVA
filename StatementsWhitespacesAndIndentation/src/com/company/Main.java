package com.company;

public class Main {

    public static void main(String[] args) {
        int myVariable = 50;
        //  myVariable = 50 is an expression;
        // int myVariable = 50; is an statement where i'm assigning the value 50 to a variable called myVariable;

        myVariable++; //is another statement;

        System.out.println("This is a test"); // this is a complete statement as well;

        System.out.println("This is a statement" +
                "this is another statement" +
                "and yet another one - a statement is finished by a semi-colon");
    }
}
